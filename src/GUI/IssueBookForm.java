    private void jButton_IssueBook_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_IssueBook_ActionPerformed

        String note = jTextArea_Note_.getText();
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            int bookID = Integer.parseInt(jTextField_BookID_.getText());
            int memberID = Integer.parseInt(jTextField_MemberID_.getText());
            String issue_date = dateFormat.format(jDateChooser_IssueDate_.getDate());
            String return_date = dateFormat.format(jDateChooser_ReturnDate_.getDate());
            
            Date issueDate = dateFormat.parse(issue_date);
            Date returnDate = dateFormat.parse(return_date);
            
            if(!book_exist) {
                JOptionPane.showMessageDialog(null, "You need to check if this book exist first when you click the search book button", "Notification", 2);
            } else if(!member_exist) {
                JOptionPane.showMessageDialog(null, "You need to check if this member exist first when you click the search member button", "Notification", 2);
            } else if(!issue_Book.checkBookAvailability(bookID)) {
                JOptionPane.showMessageDialog(null, "This book is not available right now", "Notification", 2);
            } else if(returnDate.before(issueDate)) {
                JOptionPane.showMessageDialog(null, "The Return date must be after Then Issue date", "Notification", 2);
            } else {
                issue_Book.addIssue(bookID, memberID, "issued", issue_date, return_date, note);
                
                // reset fields
                jTextField_BookID_.setText("");
                jTextField_MemberID_.setText("");
                jLabel_BookName_.setText("Book Name");
                jLabel_MemberFullname_.setText("Member Full-name");
                jLabel_Avaliable_.setText("Yes-or-No");
                jLabel_Avaliable_.setForeground(new Color(51,102,255));
                jDateChooser_IssueDate_.setDate(null);
                jDateChooser_ReturnDate_.setDate(null);
                book_exist = false;
                member_exist = false;
                
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Fill in all information", "Notification", 2);
        }
    }//GEN-LAST:event_jButton_IssueBook_ActionPerformed

