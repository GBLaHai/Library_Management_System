    private void jButton_Search_Book_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Search_Book_ActionPerformed
        
        if(jTextField_BookID_.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "The Book ID is Empty", "Notification", 2);
        } else {
            int book_id = Integer.parseInt(jTextField_BookID_.getText());
            try {
                DTO.BookDTO selected_book = book.getBookByID(book_id);
                if(selected_book != null) {
                    jLabel_BookName_.setVisible(true);
                    jLabel_BookName_.setText(selected_book.getName());
                    book_exist = true;
                    Book_id_search = book_id;
                    
                    // display availability
                    if(issue_Book.checkBookAvailability(book_id)) {
                        jLabel_Avaliable_.setText("YES");
                        jLabel_Avaliable_.setForeground(Color.green);
                    } else {
                        jLabel_Avaliable_.setText("NO");
                        jLabel_Avaliable_.setForeground(Color.red);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "This book doesn't exist", "Notification", 2);
                    book_exist = false;
                    jLabel_Avaliable_.setText("Yes-or-No");
                    jLabel_Avaliable_.setForeground(new Color(51,51,255));
                    jLabel_BookName_.setText("Book Name");
                    jLabel_BookName_.setVisible(false);
                }
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(IssueBookForm.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton_Search_Book_ActionPerformed

