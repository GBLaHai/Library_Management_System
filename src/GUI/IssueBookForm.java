    private void jButton_Search_Member_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Search_Member_ActionPerformed
        if(jTextField_MemberID_.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "The Member ID is Empty", "Notification", 2);
        } else {
            int member_id = Integer.parseInt(jTextField_MemberID_.getText());
            try {
                DTO.MemberDTO selected_member = member.getMemberByID(member_id);
                if(selected_member != null) {
                    String fullname = selected_member.getFirstName() + " " + selected_member.getLastName();
                    jLabel_MemberFullname_.setText(fullname);
                    member_exist = true;
                    Member_id_search = member_id;
                    jLabel_MemberFullname_.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "This member doesn't exist", "Notification", 2);
                    member_exist = false;
                    jLabel_MemberFullname_.setText("Member Full-name");
                    jLabel_MemberFullname_.setVisible(false);
                }
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(IssueBookForm.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton_Search_Member_ActionPerformed

