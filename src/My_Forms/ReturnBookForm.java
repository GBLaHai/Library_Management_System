    // create a function to populate the jtable with issued books
    public void populateJtableWithIssuedBooks(String status) {
        ArrayList<Issue_Book> issuedBooksList = issue_Book.issuedBookList(status);
        
        
        // jtable columns
        String[] colNames = {"Book","Member", "Status", "Iss-Date", "Rtn-Date", "Note"};
        
        // jtable rows
        Object[][] rows = new Object[issuedBooksList.size()][colNames.length];
        
        for(int i = 0; i < issuedBooksList.size(); i++) {
            rows[i][0] = issuedBooksList.get(i).getBook_id();
            rows[i][1] = issuedBooksList.get(i).getMember_id();
            rows[i][2] = issuedBooksList.get(i).getStatus();
            rows[i][3] = issuedBooksList.get(i).getIssue_date();
            rows[i][4] = issuedBooksList.get(i).getReturn_date();
            rows[i][5] = issuedBooksList.get(i).getNote();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable_Issued_Books_.setModel(model);
    }
    
