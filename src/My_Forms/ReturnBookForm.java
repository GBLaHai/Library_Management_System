    private void jTable_Issued_Books_MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_Issued_Books_MouseClicked
        // display the selected issued book
        // get the selected row data
        int index = jTable_Issued_Books_.getSelectedRow();

        // get values
        int book_id = Integer.parseInt(jTable_Issued_Books_.getValueAt(index, 0).toString());
        int member_id = Integer.parseInt(jTable_Issued_Books_.getValueAt(index, 1).toString());
        My_Classes.Book selectedBook;
        My_Classes.Member selectedMember;
        try {
            selectedBook = book.getBookByID(book_id);
            jTextField_BookID_.setText(String.valueOf(selectedBook.getId()));
            jLabel_BookName_.setText(selectedBook.getName());
            jLabel_BookName_.setVisible(true);
            Book_id_search = book_id;
            
            selectedMember = member.getMemberByID(member_id);
            jTextField_MemberID_.setText(String.valueOf(selectedMember.getId()));
            String fullname = selectedMember.getFirstName() + " " + selectedMember.getLastName();
            jLabel_MemberFullname_.setText(fullname);
            jLabel_MemberFullname_.setVisible(true);
            Member_id_search = member_id;
            
            String status = jTable_Issued_Books_.getValueAt(index, 2).toString();
            String issued_book = jTable_Issued_Books_.getValueAt(index, 3).toString();
            String return_book = jTable_Issued_Books_.getValueAt(index, 4).toString();
            String note = jTable_Issued_Books_.getValueAt(index, 5).toString();
            
            //display the date
            Date issDate = new SimpleDateFormat("yyyy-MM-dd").parse(issued_book);
            jDateChooser_IssueDate_.setDate(issDate);
            
            Date rtnDate = new SimpleDateFormat("yyyy-MM-dd").parse(return_book);
            jDateChooser_ReturnDate_.setDate(rtnDate);
            
            jTextArea_Note_.setText(note);
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBookForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(ReturnBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable_Issued_Books_MouseClicked

