    private void jButton_Return_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Return_ActionPerformed
        
        // return the book
        // we will update the return date
        // the note
        // and the status to returned
        
        String note = jTextArea_Note_.getText();
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            int bookID = Integer.parseInt(jTextField_BookID_.getText());
            int memberID = Integer.parseInt(jTextField_MemberID_.getText());
            
            String return_date = dateFormat.format(jDateChooser_ReturnDate_.getDate());
            Date returnDate = dateFormat.parse(return_date);
            
            String issue_date = dateFormat.format(jDateChooser_IssueDate_.getDate());
            Date issueDate = dateFormat.parse(issue_date);
            
            if(returnDate.before(issueDate)) {
                JOptionPane.showMessageDialog(null, "The Return date must be after Then Issue date", "Notification", 2);
            } else {
                issue_Book.updateIssue(bookID, memberID, "returned", issue_date, return_date, note);
                
                // reset fields
                jTextField_BookID_.setText("");
                jTextField_MemberID_.setText("");
                jLabel_BookName_.setText("Book Name");
                jLabel_BookName_.setVisible(false);
                jLabel_MemberFullname_.setText("Member Full-name");
                jLabel_MemberFullname_.setVisible(false);
                jDateChooser_IssueDate_.setDate(null);
                jDateChooser_ReturnDate_.setDate(new Date());
                jTextArea_Note_.setText("");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Select book which want to return from table", "Notification", 2);
        }
    }//GEN-LAST:event_jButton_Return_ActionPerformed

